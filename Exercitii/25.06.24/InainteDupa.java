public class InainteDupa {
    
    public static void main(String[] args) {
        
        int a = 5;
        int b = 3;

        System.out.println("Inainte: ");
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        System.out.println("\nDupa: ");
        System.out.println("a++ = " + a++);
        System.out.println("++b = " + ++b);
    }
}
